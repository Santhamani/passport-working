import axios from 'axios'


export const login = user => {
  return axios
    .post('http://localhost:8080/login', {
      email: user.email,
      password: user.password
    })
    .then(response => {
      localStorage.setItem('usertoken', response.data)
      return response.data
    })
    .catch(err => {
      console.log(err)
    })
}